      <h3>Biography</h3>
      <p>Samuel Bacharach is the McKelvey-Grant Professor of Labor Management and is Director of ILR's New York City based Institute for Workplace Studies as well as the Director of the Smithers Institute. He is also the director of the New York City-based Master of Professional Studies. He received his BS in economics from NYU. His MS and Ph.D. from the University of Wisconsin.</p>
      <p>Upon joining the Cornell faculty in 1974, he spent most of his time working on negotiation and organizational politics, publishing numerous articles and two volumes (Power and Politics in Organizations and Bargaining: Power, Tactics, and Outcome, both with Edward J. Lawler). In the 1980s he continued working on negotiation, but shifted emphasis to the study of complex organizations, with the empirical referent being schools. Besides his academic articles, he published a number of books on school management and leadership, such as <em>Tangled Hierarchies</em> (with Joseph Shedd) and <em>Education Reform: Making Sense of It All</em>.</p>
      <p>In the late 1980s, Professor Bacharach took over the directorship of the Smithers Institute. In this role, he began working on a number of empirical studies such as: blue-collar workers in New York City, a study of New York City firefighters after 9/11, a study of transport workers in New York City, and a ten-year longitudinal study of retired workers. With his colleagues Peter Bamberger and William Sonnenstuhl he has published numerous articles on blue-collar workers, with emphasis on substance abuse in the workplace. His collaboration with Bamberger and Sonnenstuhl resulted in two books: <em>Mutual Aid and Union Renewal: Cycles of Logics of Action</em> and <em>Member Assistance Programs: Labor's Role in the Prevention and Treatment of Substance Abuse</em>.</p>
      <p>For the last ten years, working in New York City, Professor Bacharach has focused on bridging theory and practice. Integrating his earlier work on politics, negotiation, and complex organizations into his studies of the workplace, he has re-examined the role of leadership. Working closely with practitioners from various fields, conducting workshops, training sessions, and open discussions, he has developed his own leadership perspective. This work culminated with the publication of <em>Get Them on Your Side: Win Support, Convert Skeptics, and Get Results</em> (2005) and <em>Keep Them on Your Side: Leading and Managing for Momentum</em> (2006). He has created a five-day leadership workshop and a series of online courses (with eCornell) based on this material.</p>
